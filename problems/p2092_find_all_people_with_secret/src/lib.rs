pub struct Solution {}

pub struct DisjointSet {
    parent: Vec<usize>,
    size: Vec<usize>,
}

impl DisjointSet {
    pub fn new(n: usize) -> Self {
        Self {
            parent: (0..n).collect(),
            size: vec![1; n],
        }
    }

    pub fn root(&mut self, i: usize) -> usize {
        if self.parent[i] == i {
            return i;
        }

        let parent = self.root(self.parent[i]);
        self.parent[i] = parent;
        parent
    }

    pub fn unite(&mut self, i: usize, j: usize) {
        let mut parent_i = self.root(i);
        let mut parent_j = self.root(j);

        if parent_i == parent_j {
            return;
        }

        if self.size(parent_i) < self.size(parent_j) {
            std::mem::swap(&mut parent_i, &mut parent_j);
        }

        self.parent[parent_j] = parent_i;
        self.size[parent_i] += self.size[parent_j];
    }

    pub fn reset(&mut self, i: usize) {
        self.parent[i] = i;
        self.size[i] = i;
    }

    pub fn size(&mut self, i: usize) -> usize {
        let root = self.root(i);
        self.size[root]
    }
}

impl Solution {
    pub fn find_all_people(n: i32, meetings: Vec<Vec<i32>>, first_person: i32) -> Vec<i32> {
        let mut meeting_group = vec![vec![]; 100_001];
        for m in meetings {
            meeting_group[m[2] as usize].push((m[0] as usize, m[1] as usize));
        }

        let mut ds = DisjointSet::new(n as usize);
        ds.unite(0, first_person as usize);

        for meegings in meeting_group {
            for (x, y) in &meegings {
                ds.unite(*x, *y);
            }

            for (x, y) in &meegings {
                if ds.root(0) != ds.root(*x) {
                    ds.reset(*x);
                    ds.reset(*y);
                }
            }
        }

        let mut result = Vec::new();
        for i in 0..n as usize {
            if ds.root(0) == ds.root(i) {
                result.push(i as i32);
            }
        }
        result
    }
}

