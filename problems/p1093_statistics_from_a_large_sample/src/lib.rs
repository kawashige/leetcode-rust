pub struct Solution {}

impl Solution {
    pub fn sample_stats(count: Vec<i32>) -> Vec<f64> {
        let mut result = vec![0.0; 5];

        result[0] = (0..count.len()).find(|i| count[*i] > 0).unwrap() as f64;
        result[1] = (0..count.len()).rev().find(|i| count[*i] > 0).unwrap() as f64;
        let total_count = count.iter().sum::<i32>();
        result[2] = (0..count.len())
            .map(|i| i as f64 * count[i] as f64)
            .sum::<f64>()
            / total_count as f64;
        let acc = count
            .iter()
            .scan(0, |acc, c| {
                *acc += c;
                Some(*acc)
            })
            .collect::<Vec<_>>();
        result[3] = (0..acc.len())
            .find(|i| total_count / 2 + 1 <= acc[*i])
            .unwrap() as f64;
        if total_count % 2 == 0 {
            result[3] = (result[3]
                + (0..acc.len()).find(|i| total_count / 2 <= acc[*i]).unwrap() as f64)
                / 2.0;
        }
        result[4] = (0..count.len()).max_by_key(|i| count[*i]).unwrap() as f64;

        result
    }
}

