pub struct Solution {}

impl Solution {
    pub fn rearrange_barcodes(mut barcodes: Vec<i32>) -> Vec<i32> {
        let occurrence = barcodes.iter().fold([0; 10001], |mut occurrence, x| {
            occurrence[*x as usize] += 1;
            occurrence
        });
        barcodes.sort_unstable_by(|a, b| {
            occurrence[*b as usize]
                .cmp(&occurrence[*a as usize])
                .then(a.cmp(&b))
        });

        let mut result = vec![0; barcodes.len()];
        let mut j = 0;
        for i in 0..barcodes.len() {
            result[j] = barcodes[i];
            j += 2;
            if result.len() <= j {
                j = 1;
            }
        }

        result
    }
}

